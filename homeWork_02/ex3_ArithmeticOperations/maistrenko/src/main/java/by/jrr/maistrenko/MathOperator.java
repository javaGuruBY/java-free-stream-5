package by.jrr.maistrenko;

public class MathOperator {

    public static double add(double a, double b) {
        return a + b;
    }

    public static double substractBFromA(double a, double b) {
        return a - b;
    }

    public static double multiplicate(double a, double b) {
        return a * b;
    }

    public static double divideAbyB(double a, double b) {
        return a / b;
    }
}
