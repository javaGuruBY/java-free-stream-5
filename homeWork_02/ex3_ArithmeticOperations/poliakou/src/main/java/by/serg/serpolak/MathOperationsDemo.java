package by.serg.serpolak;

public class MathOperationsDemo {
    public static void main(String[] args) {

        int a = 5;
        int b = 15;

        int summa = a +b;
        int raznost = b-a;
        int ymnogenie = a * b;
        int delenie = b / a;

        System.out.println(summa);
        System.out.println(raznost);
        System.out.println(ymnogenie);
        System.out.println(delenie);
    }
}
