package by.jrr.rakhnianski;

public class Store {
    public static void main(String[] args) {
        goToTheStore();
        giveMoney();
        takeIceCream();
        exitTheStore();
    }
    public static void goToTheStore() {
        System.out.println("Going to the store");
    }
    public static void giveMoney() {
        System.out.println("Giving money");
    }
    public static void takeIceCream() {
        System.out.println("Taking Ice cream");
    }
    public static void exitTheStore() {
        System.out.println("Exiting the store");
    }

}
